    IEnumerator GetText()
    {
        resetValues();
        UnityWebRequest www = UnityWebRequest.Get("http://localhost:3001/registros_ultima_hora");
        yield return www.SendWebRequest();

        if (www.isNetworkError)
        {
            Debug.Log(www.error);
        }
        else
        {

            var sensor = JsonConvert.DeserializeObject<SensorResponse>(www.downloadHandler.text);
            var sensorRecords = sensor.results;
            // Debug.Log(sensorRecords);

            foreach (var sensorRecord in sensorRecords)
            {
                CalculaZona(sensorRecord.Longitud, sensorRecord.Latitud, sensorRecord.Con_gas);
            }

            GenereateGrid();
        }

    }

    void GenereateGrid()
    {

        for (int x = 0; x < _width; x++)
        {
            for (int y = 0; y < _height; y++)
            {
                auxZona = GameObject.Find($"Zona-{x}-{y}").GetComponent<Zona>();

                var calcPromedioGas = 0;
                if (matrizContador[x][y] != 0)
                {
                    calcPromedioGas = matrizSuma[x][y] / matrizContador[x][y];
                }

                if (calcPromedioGas == 0)
                {
                    color = 0;
                }
                if (calcPromedioGas > 0.00 && calcPromedioGas <= 50.00)
                {
                    color = 1;
                }
                if (calcPromedioGas > 50.00 && calcPromedioGas <= 75.00)
                {
                    color = 2;
                }
                if (calcPromedioGas > 75.00 && calcPromedioGas <= 200.00)
                {
                    color = 3;
                }
                if (calcPromedioGas > 200.00 && calcPromedioGas <= 1000.00)
                {
                    color = 4;
                }
                if (calcPromedioGas > 1000.00)
                {
                    color = 5;
                }
                // Debug.Log(color);
                int lecturas = matrizContador[x][y];

                int[] aux = new int[] { color, calcPromedioGas, lecturas };
                auxZona.CambiaColor(aux);
            }
        }


    }

